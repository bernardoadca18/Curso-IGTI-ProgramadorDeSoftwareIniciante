# If/Se

a = 50;
b = 100;
c = 50;

print("------------------------");
print("A: " + str(a));
print("B: " + str(b));
print("C: " + str(c));
print("------------------------");

# Igual
if(a == c):
    print("A é igual a C");

# Diferente
if(a != b):
    print("A é diferente de B");

# Menor que
if(a < b):
    print("A é menor do que B");

# Menor ou igual a
if(a <= b):
    print("A é menor ou igual a B");

if(a <= c):
    print("A é menor ou igual a C");

# Maior que
if(b > c):
    print("B é maior do que C");

print("------------------------");